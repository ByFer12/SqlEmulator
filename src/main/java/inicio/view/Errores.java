    public void mostrarErroresSytax() {
        DefaultTableModel tbModel = (DefaultTableModel) tbSyntax.getModel();
        List<Errors> erroresSintax = parser.erroresSintacticos;

        tbModel.setRowCount(0);
        for (Errors data : erroresSintax) {
            tbModel.addRow(new Object[]{data.getLexema(), data.getTipo(), data.getDescripcion(), data.getFila(), data.getColumna()});
        }

    }

    public void mostrarErroresLexicos() {
        DefaultTableModel tbModel = (DefaultTableModel) tbLex.getModel();
        List<Errors> erroresSintax = SqlLexer.erroresLexicos;

        tbModel.setRowCount(0);
        for (Errors data : erroresSintax) {
            tbModel.addRow(new Object[]{data.getLexema(), data.getTipo(), data.getDescripcion(), data.getFila(), data.getColumna()});
        }

    }

